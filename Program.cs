using System;
using System.Collections.Generic;
using System.Linq;

Main();

void Main()
{
    MooseSays("Ask me a question, any question!");
    bool isQuestionNotValid = String.IsNullOrWhiteSpace(Console.ReadLine());
    if (!isQuestionNotValid)
    {
        MooseResponds();
    }
}

